# 判斷式
x=input("請輸入數字: ")     # 取得"字串"形式的使用者輸入，一律是字串
x=int(x)        # 將字串型態轉換成"數字"型態
if x>200:
   print("大於 200")
elif x>100:
   print("大於 100，小於等於 200")
else:
   print("小於等於 100")

