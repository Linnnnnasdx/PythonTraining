# 資料: 程式的基本單位

# 數字
1234
3.5

# 字串:用"雙引號"框起，用於表達任意文字
"測試中文"
"Hello World"

# 布林值
True
False

# List:有順序、可動的列表 用"逗點"隔開
[3,4,5]
["Hello","World"]

# Tuple:有順序、不可動的列表
(3,4,5)
("Hello","World")

# Set:集合
{3,4,5}
{"Hello","World"}

# Dictionary:字典
{"apple":"蘋果","data":"資料"}

# 變數:用來儲存資料的自訂名稱(開頭不能是數字)
X=3     # 變數名稱=資料


# print(資料)
print(X)
X=True #取代舊的資料
print(X)
X="Hello"
print(X)
X={3,5,6} #集合Set
print(X)